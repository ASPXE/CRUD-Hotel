    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        int id = 0;
        double sizeM2 = 0;
        
        if(txtId.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "ID del estacionamiento no ingresado", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            id = Integer.parseInt(txtId.getText());
        }
        if(txtSizeM2.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Area en M2 del estacionamiento no ingresada", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            sizeM2 = Double.parseDouble(txtSizeM2.getText());
        }
        
        EstacionamientoDAO ed = new EstacionamientoDAO();
        Estacionamiento e = new Estacionamiento();
        
        e.setIdEstacionamiento(id);
        e.setSizeM2(sizeM2);
        
        try {
            ed.actualizarSizeM2(e);
            JOptionPane.showMessageDialog(rootPane, "Area del estacionamiento actualizada correctamente", "Registro actualizado", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(ActualizarEstacionamientoSizeM2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

