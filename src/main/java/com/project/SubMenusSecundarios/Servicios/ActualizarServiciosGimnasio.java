    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int idServicio = 0;
        boolean gimnasio = false;
        
        if(txtIdServicio.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "ID del servicio no ingresado", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            idServicio = Integer.parseInt(txtIdServicio.getText());
        }
        if(!rbSi.isSelected() && !rbNo.isSelected()){
            JOptionPane.showMessageDialog(rootPane, "No selecciono si se incluye el gimnasio o no", "Informacion faltante", JOptionPane.ERROR_MESSAGE);
        }else{
            if(rbSi.isSelected()){
                rbNo.setSelected(false);
                gimnasio = rbSi.isSelected();
            }else if(rbNo.isSelected()){
                rbSi.setSelected(false);
                gimnasio = rbNo.isSelected();
            }
        }
        
        ServiciosDAO sd = new ServiciosDAO();
        Servicios s = new Servicios();
        
        s.setIdServicios(idServicio);
        s.setGimnasio(gimnasio);
        
        try {
            sd.actualizarGimnasio(s);
            JOptionPane.showMessageDialog(rootPane, "Gimnasio actualizado exitosamente", "Registro actualizado", JOptionPane.ERROR_MESSAGE);
                    
        } catch (SQLException ex) {
            Logger.getLogger(ActualizarServiciosGimnasio.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

